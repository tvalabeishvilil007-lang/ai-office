function sleep(ms: number): Promise<void> {
  return new Promise((r) => setTimeout(r, ms));
}

function sseChunk(res: Response, text: string) {
  res.write(`data: ${JSON.stringify({ type: 'chunk', text })}\n\n`);
}

function sseDone(res: Response) {
  res.write(`data: ${JSON.stringify({ type: 'done' })}\n\n`);
  res.end();
}

// Stream text word-by-word to mimic AI typing
async function streamWords(res: Response, text: string): Promise<void> {
  // Split preserving newlines as tokens
  const tokens: string[] = [];
  const lines = text.split('\n');
  for (let i = 0; i < lines.length; i++) {
    const words = lines[i].split(' ');
    for (const w of words) {
      if (w) tokens.push(w + ' ');
    }
    if (i < lines.length - 1) tokens.push('\n');
  }

  for (const token of tokens) {
    sseChunk(res, token);
    // Vary speed: faster for short words, slower for long ones
    const delay = token.trim().length > 6 ? 35 + Math.random() * 30 : 20 + Math.random() * 20;
    await sleep(delay);
  }
}

